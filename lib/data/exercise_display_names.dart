/// Clean display names for all 534 exercises
/// Maps exercise IDs from ExerciseGifMapping to human-readable names
///
/// Rules applied:
/// - Equipment moved to end in parentheses: "Bench Press (Barbell)"
/// - lever_ prefix → (Machine)
/// - smith_ prefix → (Smith Machine)
/// - sled_ prefix → (Sled)
/// - ez_barbell_ / ez_bar_ prefix → (EZ Bar)
/// - Suffixes removed: _m, _f, _male, _female, _version_N, _ii, _iii, _iv, _plate_loaded
/// - Common names used where applicable

class ExerciseDisplayNames {
  static String getName(String exerciseId) {
    return _names[exerciseId] ?? _autoFormat(exerciseId);
  }

  static String _autoFormat(String id) {
    // Strip junk suffixes
    String cleaned = id;
    cleaned = cleaned.replaceAll(RegExp(r'_version_\d+$'), '');
    cleaned = cleaned.replaceAll(RegExp(r'_ii$'), '');
    cleaned = cleaned.replaceAll(RegExp(r'_iii$'), '');
    cleaned = cleaned.replaceAll(RegExp(r'_iv$'), '');
    cleaned = cleaned.replaceAll(RegExp(r'_plate_loaded$'), '');
    cleaned = cleaned.replaceAll(RegExp(r'_fix$'), '');
    cleaned = cleaned.replaceAll(RegExp(r'_male$'), '');
    cleaned = cleaned.replaceAll(RegExp(r'_female$'), '');
    if (cleaned.endsWith('_m') && !cleaned.endsWith('_arm')) {
      cleaned = cleaned.substring(0, cleaned.length - 2);
    }
    if (cleaned.endsWith('_f')) {
      cleaned = cleaned.substring(0, cleaned.length - 2);
    }

    // Detect equipment prefix
    String equipment = '';
    if (cleaned.startsWith('lever_') || cleaned.startsWith('leverage_')) {
      equipment = 'Machine';
      cleaned = cleaned.startsWith('leverage_')
          ? cleaned.substring(9)
          : cleaned.substring(6);
    } else if (cleaned.startsWith('smith_machine_')) {
      equipment = 'Smith Machine';
      cleaned = cleaned.substring(14);
    } else if (cleaned.startsWith('smith_')) {
      equipment = 'Smith Machine';
      cleaned = cleaned.substring(6);
    } else if (cleaned.startsWith('sled_')) {
      equipment = 'Sled';
      cleaned = cleaned.substring(5);
    } else if (cleaned.startsWith('ez_barbell_')) {
      equipment = 'EZ Bar';
      cleaned = cleaned.substring(11);
    } else if (cleaned.startsWith('ez_bar_')) {
      equipment = 'EZ Bar';
      cleaned = cleaned.substring(7);
    } else if (cleaned.startsWith('barbell_')) {
      equipment = 'Barbell';
      cleaned = cleaned.substring(8);
    } else if (cleaned.startsWith('dumbbell_') || cleaned.startsWith('dumbbells_')) {
      equipment = 'Dumbbell';
      cleaned = cleaned.startsWith('dumbbells_')
          ? cleaned.substring(10)
          : cleaned.substring(9);
    } else if (cleaned.startsWith('cable_')) {
      equipment = 'Cable';
      cleaned = cleaned.substring(6);
    } else if (cleaned.startsWith('machine_')) {
      equipment = 'Machine';
      cleaned = cleaned.substring(8);
    } else if (cleaned.startsWith('band_')) {
      equipment = 'Band';
      cleaned = cleaned.substring(5);
    } else if (cleaned.startsWith('kettlebell_')) {
      equipment = 'Kettlebell';
      cleaned = cleaned.substring(11);
    }

    // Strip remaining junk suffixes after equipment removal
    cleaned = cleaned.replaceAll(RegExp(r'_plate_loaded$'), '');

    // Capitalize words
    final words = cleaned.split('_').where((w) => w.isNotEmpty).toList();
    final name = words.map((word) {
      if (word.isEmpty) return '';
      return word[0].toUpperCase() + word.substring(1);
    }).join(' ');

    if (equipment.isNotEmpty) {
      return '$name ($equipment)';
    }
    return name;
  }

